#o Sinal de '+' pode ser usado para concatenação e para soma (prestar atenção ao usar)
concatenacao = 'Renato '+'Peixoto '+'de '+'Almeida';
print(concatenacao);

#o sinal de * (vezez/multiplicação) pode ser usado para repetir algo X quantidade de vezes (tomar cuidado ao usar também)
adezvezes = 'A' * 10;
print(adezvezes);

oi_sete_vezes = 7 * 'oi'
print(oi_sete_vezes)
