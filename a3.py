# sep="" >> é usado para definir o que será usado para separação de str, int, float e bool
print(1234)

# Aspas simples
print('Renato Peixoto')
print(1.8 ,'Renato "Peixoto"')


# Aspas duplas
print(2.2, "Renato 'Peixoto'")

# Escape é para ser usado quando você quer usar o caractére do qual foi iniciado ex: "Renato Peixoto", eu quero colocar "Peixoto" em aspas duplas, aí se usa o escape
print("Renato \"Peixoto\"")

# r é usado para quando você quer que mostr o \" \" (é usado para expressões regulares será explicado mais para frente)
print(r"Renato \"Peixoto\"")

#Escape não é muito comum pois você pode simplesmente inverter as áspas